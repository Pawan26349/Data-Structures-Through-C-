#include<iostream>
using namespace std;
int main()
{
    // Data types 
    int a;
    cout<<"The size of the int is : "<<sizeof(a)<<endl;
    float b;
    cout<<"The size of the float is : "<<sizeof(b)<<endl;
    char c;
    cout<<"The size of the char is : "<<sizeof(c)<<endl;
    bool d;
    cout<<"The size of the bool is : "<<sizeof(d)<<endl;
    double i;
    cout<<"The size of the double is : "<<sizeof(i)<<endl;


    // Data Modifiers 
    signed int e;
    cout<<"The size of the signed int : "<<sizeof(e)<<endl;
    unsigned int  f;
    cout<<"The size of the signed int : "<<sizeof(f)<<endl;
    short int g;
    cout<<"The size of the signed int : "<<sizeof(g)<<endl;
    long int h;
    cout<<"The size of the signed int : "<<sizeof(h)<<endl;


    return 0;
}